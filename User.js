class User {
    constructor(id, name, age, gender, email)
    {
        this.id = id;
        this.name = name;
        this.age = age;
        this.gender = gender;
        this.email = email;
    }

    get id()
    {
        return this.id;
    }

    get name()
    {
        return this.name;
    }

    get age()
    {
        return this.age;
    }

    get gender()
    {
        return this.gender;
    }

    get email()
    {
        return this.email;
    }
}